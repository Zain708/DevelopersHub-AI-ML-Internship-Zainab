import pandas as pd # for the data manipulation like putting the data in tables (Excel Sheet)
import seaborn as sns # for building plots and also it has iris dataset ready for use
import matplotlib.pyplot as plt # for builing plots and seaborn use this as it's foundation or engine
df = sns.load_dataset('iris') # loading the dataset using seaborn's built-in function sns.load_dataset('name of dataset') in variable df (DataFrame)
print(df.head(5)) # printing the first 5 rows of dataset using pandas' .head() function

# Using Pandas built-in tools to inspect the dataset:
# - Check total size and missing data
# - Calculate statistical averages (like petal length)

# - A attribute is a fact (it is something), It does not use parentheses because it just looks up a piece of information that is already stored
dataset_shape = df.shape # tells you the dimensions of the dataset as (rows, columns)
dataset_columns = df.columns # lists all the column names
print(dataset_shape)
print(dataset_columns)

# - A function is an action (it does something), It uses parentheses () because it has to run code to calculate a result
print(df.info()) # This function (needs parentheses) gives a concise summary of the DataFrame, including the data types of each column and if there are any missing (null) values
print(df.describe()) # # This function calculates summary statistics (like mean, minimum, maximum, and percentiles) for all the numerical columns

sns.scatterplot(data = df, x = 'sepal_length', y = 'sepal_width', hue = 'species' )
plt.show() # plot displayed by matplotlib

sns.histplot(data = df,x = 'petal_length', kde = True)
plt.show()

sns.boxplot(data = df, x ='species', y = 'sepal_length')
plt.show()

sns.boxplot(data = df, x ='species', y = 'sepal_width')
plt.show()

